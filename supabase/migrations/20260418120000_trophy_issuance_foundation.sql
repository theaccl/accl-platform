-- Trophy issuance foundation (trusted + idempotent).
-- This migration does NOT define award rules; it only provides secure issuance plumbing.

alter table public.trophy_records
  add column if not exists milestone_key text null;

alter table public.trophy_records
  drop constraint if exists trophy_records_milestone_key_not_blank;

alter table public.trophy_records
  add constraint trophy_records_milestone_key_not_blank check (
    milestone_key is null or btrim(milestone_key) <> ''
  );

comment on column public.trophy_records.milestone_key is
  'Idempotency key from trusted trophy milestone orchestrator.';

create unique index if not exists trophy_records_user_milestone_key_uidx
  on public.trophy_records (user_id, milestone_key)
  where milestone_key is not null;

create table if not exists public.trophy_issuance_audit (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  emitter text not null,
  user_id uuid null references public.profiles (id) on delete set null,
  milestone_key text null,
  source_game_id uuid null references public.games (id) on delete set null,
  source_tournament_id uuid null references public.tournaments (id) on delete set null,
  outcome text not null
    constraint trophy_issuance_audit_outcome_check check (
      outcome in ('issued', 'already_issued', 'skipped', 'error')
    ),
  details jsonb not null default '{}'::jsonb
);

comment on table public.trophy_issuance_audit is
  'Trusted audit trail for trophy issuance outcomes.';

create index if not exists trophy_issuance_audit_user_idx
  on public.trophy_issuance_audit (user_id, created_at desc);

create index if not exists trophy_issuance_audit_milestone_idx
  on public.trophy_issuance_audit (milestone_key, created_at desc);

alter table public.trophy_issuance_audit enable row level security;

revoke all on table public.trophy_issuance_audit from public;
revoke all on table public.trophy_issuance_audit from anon;
revoke all on table public.trophy_issuance_audit from authenticated;

create or replace function public.issue_trophy_record(
  p_user_id uuid,
  p_title text,
  p_category text,
  p_date_awarded timestamptz default null,
  p_source_game_id uuid default null,
  p_source_tournament_id uuid default null,
  p_placement integer default null,
  p_level text default null,
  p_description text default null
)
returns public.trophy_records
language plpgsql
security definer
set search_path = public
as $$
declare
  v_title text;
  v_category text;
  v_level text;
  v_desc text;
  v_row public.trophy_records%rowtype;
begin
  if p_user_id is null then
    raise exception 'user_id is required';
  end if;

  v_title := trim(coalesce(p_title, ''));
  if v_title = '' then
    raise exception 'title is required';
  end if;

  v_category := trim(coalesce(p_category, ''));
  if v_category = '' then
    raise exception 'category is required';
  end if;

  if p_placement is not null and p_placement <= 0 then
    raise exception 'placement must be positive when provided';
  end if;

  v_level := nullif(trim(coalesce(p_level, '')), '');
  v_desc := nullif(trim(coalesce(p_description, '')), '');

  insert into public.trophy_records (
    user_id,
    title,
    category,
    date_awarded,
    source_game_id,
    source_tournament_id,
    placement,
    level,
    description
  )
  values (
    p_user_id,
    v_title,
    v_category,
    p_date_awarded,
    p_source_game_id,
    p_source_tournament_id,
    p_placement,
    v_level,
    v_desc
  )
  returning * into v_row;

  return v_row;
end;
$$;

comment on function public.issue_trophy_record(
