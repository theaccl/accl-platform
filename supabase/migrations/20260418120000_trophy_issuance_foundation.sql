create or replace function public.orchestrate_trophy_issuance(
  p_user_id uuid,
  p_milestone_key text,
  p_title text,
  p_category text,
  p_date_awarded timestamptz default null,
  p_source_game_id uuid default null,
  p_source_tournament_id uuid default null,
  p_placement integer default null,
  p_level text default null,
  p_description text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_key text;
  v_existing public.trophy_records%rowtype;
  v_created public.trophy_records%rowtype;
begin
  if p_user_id is null then
    raise exception 'user_id is required';
  end if;

  v_key := lower(trim(coalesce(p_milestone_key, '')));
  if v_key = '' then
    raise exception 'milestone_key is required';
  end if;

  select *
    into v_existing
  from public.trophy_records
  where user_id = p_user_id
    and milestone_key = v_key
  limit 1;

  if found then
    insert into public.trophy_issuance_audit (
      emitter, user_id, milestone_key, source_game_id, source_tournament_id, outcome, details
    )
    values (
      'orchestrate_trophy_issuance',
      p_user_id,
      v_key,
      p_source_game_id,
      p_source_tournament_id,
      'already_issued',
      jsonb_build_object('trophy_id', v_existing.id, 'reason', 'already_issued')
    );

    return jsonb_build_object(
      'issued', false,
      'reason', 'already_issued',
      'trophy_id', v_existing.id,
      'milestone_key', v_existing.milestone_key
    );
  end if;

  select *
    into v_created
  from public.issue_trophy_record(
    p_user_id,
    p_title,
    p_category,
    p_date_awarded,
    p_source_game_id,
    p_source_tournament_id,
    p_placement,
    p_level,
    p_description
  );

  begin
    update public.trophy_records
    set milestone_key = v_key
    where id = v_created.id
    returning * into v_created;
  exception
    when unique_violation then
      select *
        into v_existing
      from public.trophy_records
      where user_id = p_user_id
        and milestone_key = v_key
      limit 1;

      insert into public.trophy_issuance_audit (
        emitter, user_id, milestone_key, source_game_id, source_tournament_id, outcome, details
      )
      values (
        'orchestrate_trophy_issuance',
        p_user_id,
        v_key,
        p_source_game_id,
        p_source_tournament_id,
        'already_issued',
        jsonb_build_object('trophy_id', v_existing.id, 'reason', 'already_issued_concurrent')
      );

      return jsonb_build_object(
        'issued', false,
        'reason', 'already_issued',
        'trophy_id', v_existing.id,
        'milestone_key', v_existing.milestone_key
      );
  end;

  insert into public.trophy_issuance_audit (
    emitter, user_id, milestone_key, source_game_id, source_tournament_id, outcome, details
  )
  values (
    'orchestrate_trophy_issuance',
    p_user_id,
    v_key,
    p_source_game_id,
    p_source_tournament_id,
    'issued',
    jsonb_build_object('trophy_id', v_created.id, 'reason', 'ok')
  );

  return jsonb_build_object(
    'issued', true,
    'reason', 'ok',
    'trophy_id', v_created.id,
    'milestone_key', v_created.milestone_key
  );
exception
  when others then
    insert into public.trophy_issuance_audit (
      emitter, user_id, milestone_key, source_game_id, source_tournament_id, outcome, details
    )
    values (
      'orchestrate_trophy_issuance',
      p_user_id,
      v_key,
      p_source_game_id,
      p_source_tournament_id,
      'error',
      jsonb_build_object('error', sqlerrm)
    );
    raise;
end;
$$;

